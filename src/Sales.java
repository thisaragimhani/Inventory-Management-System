    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        PreparedStatement st;
        ResultSet rs;
        
        String pid = jTextField1.getText();
        String invoice = jTextField2.getText();
//        String date = jXDatePicker1;
        String quantity = jTextField4.getText();
        String amount = jTextField5.getText();
        SimpleDateFormat dateformat = new SimpleDateFormat("yyyy-MM-dd");
        String edate;
        edate = dateformat.format(jXDatePicker1.getDate());
        
        
        
        String sql = "INSERT INTO customer(`PrductID`, `InvoiceNumber`, `Qauntity`, `Date`, `Amount`) VALUES (?,?,?,?,?)";
        try{
        st = Dbconnect.getConnection().prepareStatement(sql);
        
        
        st.setString(1,pid);
        st.setString(2,invoice);
        st.setString(3,quantity);
        st.setString(4,edate);
        st.setString(5,amount);
        JOptionPane.showMessageDialog(null, "Inserted successfully");
        st.execute();
        
        }catch(Exception c){
            JOptionPane.showMessageDialog(null, c);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

